/**
 * Generate the component template logic
 * @param   { Object } options - options needed to generate the template
 * @returns { Promise } output - async object output
 * @returns { Object } output.template - the template structure that will be static
 * @returns { Object } output.bindings - the expressions bindings
 */
export default function template(source, options) {
  return new Promise(resolve => {
    return resolve(source)
  })
}